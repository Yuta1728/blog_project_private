# ======================================================================
# constants.py — アプリ全体で共有する定数の一元管理
# ======================================================================
#
# 【役割】
#   アプリ全体で共有する「定数」を一元管理するファイル。
#
#   【なぜ定数ファイルを分けるのか】
#   もし admin.py と blog.py にそれぞれ同じリストを書くと、
#   ジャンルを追加・削除したいときに 2 か所を修正しなければならず
#   片方だけ更新するバグが起きやすい。
#   ここに一か所まとめることで「変更は 1 か所だけ」になる（DRY 原則）。
#
# 【このファイルの構成（目次）】
#   [1] GENRE_GROUPS   : カテゴリ（グループ）付きのジャンル定義（唯一の情報源）
#   [2] DEFAULT_GENRES : GENRE_GROUPS から自動生成するフラットなジャンル一覧
#
# 【処理フロー図（利用のされ方）】
#
#   constants.GENRE_GROUPS
#        │
#        └──► views/blog.py : genre_list()
#               ジャンル一覧ページ（genre.html）のアコーディオン表示に使用
#
#   constants.DEFAULT_GENRES（GENRE_GROUPS から派生）
#        │
#        ├──► views/admin.py : _get_genre_list()
#        │      投稿フォームのジャンル選択肢の「母集合」として使用
#        │
#        └──► views/blog.py : genre_list() / index()
#               検索エリアのジャンル選択肢・独自ジャンル判定に使用
#
# ======================================================================


# ======================================================================
# [1] GENRE_GROUPS : カテゴリ付きジャンル定義（唯一の情報源 = Single Source of Truth）
# ======================================================================
#
# 【修正の背景】
# 以前はジャンルの「グループ分け（ライフスタイル / 娯楽 …）」と
# 各グループに属するジャンルが templates/genre.html にベタ書きされており、
# ここ（constants.py）にジャンルを追加しても genre.html を手で直さない限り
# 一覧ページに反映されなかった（DRY 原則の崩れ）。
#
# 【この修正でやること】
# グループ構造そのものをここに集約し、genre.html はこの定義を
# ループで描画するだけにする。これにより
#   ・ジャンルの追加/削除/グループ変更はこのファイル 1 か所で完結
#   ・genre.html の手修正が不要
# となる。
#
# - タプルの並び順・各リストの並び順が、そのまま
#   ジャンル一覧ページ（アコーディオン）の表示順になる。
# - ユーザーが「+ 新しいジャンルを作成する」で独自追加したジャンルは
#   ここには含まれないが、blog.py 側で「その他」グループとして
#   一覧ページ末尾に自動表示される（DB に実在する分のみ）。
GENRE_GROUPS = [
    ('ライフスタイル', ['日常', '健康', '旅行', '趣味', 'イラスト']),
    ('社会・経済',     ['ニュース', '経済', '投資']),
    ('技術・勉強',     ['プログラミング学習', '開発記録', '資格勉強', '勉強']),
    ('スポーツ',       ['サッカー', '野球', '競馬']),
    ('娯楽',           ['アニメ', '漫画', '本', 'ゲーム', '音楽',
                        '国内映画', '海外映画', '国内ドラマ', '海外ドラマ']),
    ('仕事',           ['バイト', '就活', '仕事']),
]


# ======================================================================
# [2] DEFAULT_GENRES : GENRE_GROUPS から自動生成するフラットなジャンル一覧
# ======================================================================
#
# GENRE_GROUPS を平坦化して 1 次元のリストにしたもの。
# アプリの他の場所（投稿フォームの選択肢・検索エリア・独自ジャンル判定など）は
# グループ構造を必要としないため、このフラットなリストを使う。
#
# GENRE_GROUPS から派生させることで「ジャンルの正本は GENRE_GROUPS だけ」
# という状態を保ち、二重管理を排除している。
# - 順番は GENRE_GROUPS の定義順（＝ジャンル選択 UI の表示順）
# - ユーザーが記事に使った独自ジャンルはこのリストには追加されず、
#   あくまで「選択肢のひな形」として機能する
DEFAULT_GENRES = [genre for _group_name, genres in GENRE_GROUPS for genre in genres]