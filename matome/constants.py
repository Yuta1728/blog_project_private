# ======================================================================
# constants.py — アプリ全体で共有する定数の一元管理
# ======================================================================
#
# 【役割】
#   アプリ全体で共有する「定数」を一元管理するファイル。
#
#   【なぜ定数ファイルを分けるのか】
#   もし admin.py と blog.py にそれぞれ同じリストを書くと、
#   ジャンルを追加・削除したいときに 2 か所を修正しなければならず
#   片方だけ更新するバグが起きやすい。
#   ここに一か所まとめることで「変更は 1 か所だけ」になる（DRY 原則）。
#
# 【このファイルの構成（目次）】
#   [1] DEFAULT_GENRES : デフォルトジャンル一覧
#
# 【処理フロー図（利用のされ方）】
#
#   constants.DEFAULT_GENRES
#        │
#        ├──► views/admin.py : _get_genre_list()
#        │      投稿フォームのジャンル選択肢の「母集合」として使用
#        │
#        └──► views/blog.py : genre_list() / index()
#               ジャンル一覧ページ・検索エリアの表示に使用
#
# ======================================================================


