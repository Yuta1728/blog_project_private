# constants.py
#
# 【役割】
# アプリ全体で共有する「定数」を一元管理するファイル。
#
# 【なぜ定数ファイルを分けるのか】
# もし admin.py と blog.py にそれぞれ同じリストを書くと、
# ジャンルを追加・削除したいときに 2 か所を修正しなければならず
# 片方だけ更新するバグが起きやすい。
# ここに一か所まとめることで「変更は 1 か所だけ」になる（DRY 原則）。
#
# 使用箇所:
#   views/admin.py  → _get_genre_list() で新規ジャンル選択肢の母集合として使用
#   views/blog.py   → genre_list() でジャンル一覧ページの表示に使用

