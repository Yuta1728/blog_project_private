# ======================================================================
# config.py — 環境変数の読み込み・提供
# ======================================================================
#
# 【役割】
#   .env ファイルから環境変数を読み込み、
#   アプリ全体で使いやすい変数名で提供するモジュール。
#
# 【このファイルの構成（目次）】
#   [1] .env ファイルの読み込み（load_dotenv / 絶対パス指定）
#   [2] PostgreSQL 接続情報（ローカル開発用・任意）
#   [3] 管理者認証情報
#   [4] ADMIN_LOGIN_PATH 未設定チェック（フェイルクローズ）
#
# ======================================================================

import os
from dotenv import load_dotenv  # python-dotenv: .env ファイルを読み込むライブラリ


# ======================================================================
# [1] .env ファイルの読み込み
# ======================================================================
# 【修正】.env をこのファイル（config.py）と同じディレクトリから明示的に読み込む。
#
# 従来は load_dotenv()（引数なし）だったが、これはカレントディレクトリを
# 基準に .env を探すため、PythonAnywhere の WSGI からアプリが import された際に
# 「カレントディレクトリがプロジェクト外」になり .env が読み込まれないことがあった。
# config.py の絶対パスから .env の場所を特定することで、
# どこから起動されても確実にプロジェクト直下の .env を読み込めるようにする。
_BASEDIR = os.path.abspath(os.path.dirname(__file__))
load_dotenv(os.path.join(_BASEDIR, '.env'))


# ======================================================================
# [2] PostgreSQL 接続情報（ローカル開発用・任意）
# ======================================================================
# SQLite 運用（PythonAnywhere など）では使われないため未設定でも問題ない。
# ローカルで docker-compose の PostgreSQL に接続する場合のみ必要。
postgre_user     = os.getenv("POSTGRES_USER")      # DB ユーザー名
postgre_password = os.getenv("POSTGRES_PASSWORD")  # DB パスワード
postgre_DB       = os.getenv("POSTGRES_DB")        # DB 名


# ======================================================================
# [3] 管理者認証情報
# ======================================================================
# ADMIN_USERNAME   : ログインフォームで入力するユーザー名
# ADMIN_PASSWORD   : 管理者パスワード
#                    （ハッシュ済み文字列でも平文でも可。init_db.py が適切に処理する）
# ADMIN_LOGIN_PATH : ログインページの URL パス（推測されにくいランダム文字列）
ADMIN_USERNAME   = os.getenv("ADMIN_USERNAME")
ADMIN_PASSWORD   = os.getenv("ADMIN_PASSWORD")
ADMIN_LOGIN_PATH = os.getenv("ADMIN_LOGIN_PATH")


# ======================================================================
# [4] ADMIN_LOGIN_PATH 未設定チェック（フェイルクローズ）
# ======================================================================
# 設定漏れをアプリ起動前の最も早い段階で検出する。
if not ADMIN_LOGIN_PATH:
    raise ValueError("【重大なエラー】環境変数 'ADMIN_LOGIN_PATH' が設定されていません。")